// 8px spacing system
const baseSpacing = 8;

const spacing = {
  none: 0,
  xs: baseSpacing / 2, // 4
  sm: baseSpacing, // 8
  md: baseSpacing * 2, // 16
  lg: baseSpacing * 3, // 24
  xl: baseSpacing * 4, // 32
  xxl: baseSpacing * 6, // 48
  xxxl: baseSpacing * 8, // 64
  huge: baseSpacing * 12, // 96
};

export default spacing;